# Определяем основной диск (обычно /dev/sda или /dev/nvme0n1)
DISK="/dev/sda"

# Проверяем, существует ли диск
if [ ! -e "$DISK" ]; then
    DISK="/dev/nvme0n1"
fi

echo "Будет очищен диск: $DISK"
echo "Нажмите Ctrl+C для отмены или Enter для продолжения..."
read

# Заполняем диск нулями (медленно, но надежно)
echo "Заполняем диск нулями..."
sudo dd if=/dev/zero of=$DISK bs=1M status=progress

# Или быстрая очистка только начала диска (достаточно для удаления ОС)
# sudo dd if=/dev/zero of=$DISK bs=1M count=100

